const inputs = document.querySelectorAll("input");

// value
inputs[0].value; // This will return whatever the value you is of the first input 

// href
const aTag = document.querySelector("a");
aTag.href; // This will return the link associated with the element 
aTag.href = "www.google.com"; // This can change the href for the a tag you selected 

// src
const image = document.querySelector("img");
image.src; // This will return the src for the img tag that you selected, you can also change the image like hrefs for links









